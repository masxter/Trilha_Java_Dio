package metodos;

public class Calculadora {
    

        
        public static void tabuadaAdicao(int a, int b){
            int resultado =  a + b;

            System.out.println("Resultado dessa soma e : " + resultado);
        }
    
        public static void tabuadaSubtracao(int a, int b){
            int resultado = a - b;
            System.out.println("Resultado da subtracao: " + resultado);
        }

        public static void tabuadaMultiplicacao(int a, int b){
            int resultado = a * b;
            System.out.println("Resultado da subtracao: " + resultado);
        }

        public static void tabuadaDivisao(int a, int b){
            int resultado = a / b;
            System.out.println("Resultado da subtracao: " + resultado);
        }

   
}
